<template>
  <div class="login_container">
    <div class="login_box">
      <div class="avatar_box">
        <img alt="#" src="@/assets/logo.png"/>
      </div>
      <el-form ref="loginFormRef" :model="loginForm" :rules="rules" class="login_form" label-width="0px">
        <el-form-item prop="username">
          <el-input
            v-model="loginForm.username"
            prefix-icon="iconfont icon-user"
          ></el-input>
        </el-form-item>
        <el-form-item prop="password">
          <el-input
            v-model="loginForm.password"
            prefix-icon="iconfont icon-3702mima"
            type="password"
          ></el-input>
        </el-form-item>
        <el-form-item class="btns_login">
          <el-button round type="primary" @click="login">登录</el-button>
          <el-button round type="info" @click="resetLoginForm">重置</el-button>
        </el-form-item>
      </el-form>
    </div>
  </div>
</template>

<script>
export default {
  data () {
    return {
      loginForm: {
        username: 'admin',
        password: '123456'
      },
      rules: {
        username: [
          {
            required: true,
            message: '请输入用户名',
            trigger: 'blur'
          },
          {
            min: 3,
            max: 10,
            message: '长度在 3 到 10 个字符',
            trigger: 'blur'
          }
        ],
        password: [
          {
            required: true,
            message: '请输入密码',
            trigger: 'blur'
          },
          {
            min: 6,
            max: 15,
            message: '长度在 6 到 15 个字符',
            trigger: 'blur'
          }
        ]
      }
    }
  },
  methods: {
    resetLoginForm () {
      this.$refs.loginFormRef.resetFields()
    },
    login () {
      this.$refs.loginFormRef.validate(async valid => {
        if (valid) {
          const { data: res } = await this.$http.post('login', this.loginForm)
          if (res.meta.status !== 200) {
            return this.$notify.error({
              title: res.meta.msg,
              message: ''
            })
          }
          this.$notify.success({
            title: '登录成功',
            message: ''
          })
          // 存储token
          window.sessionStorage.setItem('token', res.data.token)
          this.$store.commit('setUsername', res.data.username)
          // 导航到 home
          await this.$router.push('/home')
        }
      })
    }
  }
}
</script>

